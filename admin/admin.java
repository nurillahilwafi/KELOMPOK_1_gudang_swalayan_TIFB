    private void tbladminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbladminMouseClicked
        // TODO add your handling code here:
        int barisnya = tbladmin.rowAtPoint(evt.getPoint());
        
        String nb = tbladmin.getValueAt(barisnya, 0).toString();
        txtuser.setText(nb);
        String np = tbladmin.getValueAt(barisnya, 1).toString();
        txtnama.setText(np);
        String nk = tbladmin.getValueAt(barisnya, 2).toString();
        txtpwd.setText(nk);
    }//GEN-LAST:event_tbladminMouseClicked

