    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        String kodebarangkeluar = kodeTransaksi;
        String kodebarang = cb_kode_barang.getSelectedItem().toString();
        String namabarang = txtnama.getText().toString();
        String qty = txtqty.getText().toString();
        String hargaStok = txthargastok.getText().toString();
        String kodeSupliyer = cb_kode_supliyer.getSelectedItem().toString();

        // query untuk melakukan tambah data
        try {
            String sql = "insert into barang_keluar(kd_barang_keluar, kd_barang, nama_barang, qty_keluar, tgl_kadaluarsa, harga_stok, kd_supliyer) "
                    + "values (?,?,?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, kodebarangkeluar);
            pst.setString(2, kodebarang);
            pst.setString(3, namabarang);
            pst.setString(4, qty);
            String format = "yyyy-MM-dd";
            SimpleDateFormat sdf = new SimpleDateFormat(format);
            String gtl = sdf.format(dc_kadaluarsa.getDate());
            pst.setString(5, gtl);
            pst.setString(6, hargaStok);
            pst.setString(7, kodeSupliyer);
            pst.execute();
            JOptionPane.showMessageDialog(this, "Tambah barang keluar berhasil");
            // tampilkan data barang masuk
            getData();
            // kosongkan field
            btn_baruActionPerformed(evt);
            kodeTransaksiOtomatis();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e);
            System.out.println(e.getMessage());
        }

    }                                         

