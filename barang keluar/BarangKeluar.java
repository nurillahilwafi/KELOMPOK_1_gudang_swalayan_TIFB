    private void kodeTransaksiOtomatis() {
        String sql = "select * from barang_keluar order by kd_barang_keluar desc";
        try {
            stat = conn.createStatement();
            res = stat.executeQuery(sql);
            if (res.next()) {
                kode = res.getString("kd_barang_keluar").substring(1);
                String autoNumber = "" + (Integer.parseInt(kode) + 1);
                String nol = "";

                switch (autoNumber.length()) {
                    case 1:
                        nol = "00";
                        break;
                    case 2:
                        nol = "0";
                        break;
                    case 3:
                        nol = "";
                        break;
                    default:
                        break;
                }

                kodeTransaksi = "K" + nol + autoNumber;
                System.out.println(kodeTransaksi);
            } else {
                kodeTransaksi = "K001";
                System.out.println(kodeTransaksi);
            }
        } catch (NumberFormatException | SQLException e) {
            System.out.println("tidak ada data yang masuk");
            System.out.println(e.getMessage());
        }
    }

